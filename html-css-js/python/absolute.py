import statistics    #Importing statistics module
data_set=[5,2,7,4,2,6,8,9,7,3,7]
print(statistics.mean(data_set))    #Returns mean of the numbers in the list,i.e.,5.45
print(statistics.median(data_set))  #Returns middle value of the numeric data in list,i.e.,6
print(statistics.mode(data_set))    #returns most common data that occurs in list,i.e.,7






















